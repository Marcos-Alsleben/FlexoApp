    public void listarTipoCliche() {
        TipoClicheDAO dao = new TipoClicheDAO();
        List<TipoCliche> lista = dao.listarTipoCliche();
        DefaultTableModel dados = (DefaultTableModel) jT_tipoCliche.getModel();
        dados.setNumRows(0);
        for (TipoCliche c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getCriado(),
                c.getModificado()});
        }
    }

