    private void jT_tipoClicheMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jT_tipoClicheMouseClicked

        jT_tipoCliche.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() == 2) {
                    txt_idTipoCliche.setText(jT_tipoCliche.getValueAt(jT_tipoCliche.getSelectedRow(), 0).toString());
                    txt_nomeTipoCliche.setText(jT_tipoCliche.getValueAt(jT_tipoCliche.getSelectedRow(), 1).toString());

                    //desabilita botões
                    btn_novoTipoCliche.setEnabled(false);
                    btn_alterarTipoCliche.setEnabled(true);
                    btn_excluirTipoCliche.setEnabled(true);
                    btn_limparTipoCliche.setEnabled(true);

                }
            }
        });
    }//GEN-LAST:event_jT_tipoClicheMouseClicked

